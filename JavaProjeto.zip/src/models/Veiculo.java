package models;

/**
 *
 * @author User
 */
public class Veiculo {
    private int id;
    private String modelo, placa, preco;

    public Veiculo(){}
    public Veiculo(int id, String modelo, String placa, String preco){
        this.id = id;
        this.modelo = modelo;
        this.placa = placa;
        this.preco = preco;
    }

    public Veiculo(String modelo, String placa, String preco) {
        this.modelo = modelo;
        this.placa = placa;
        this.preco = preco;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the modelo
     */
    public String getModelo() {
        return modelo;
    }

    /**
     * @param modelo the modelo to set
     */
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    /**
     * @return the placa
     */
    public String getPlaca() {
        return placa;
    }

    /**
     * @param placa the placa to set
     */
    public void setPlaca(String placa) {
        this.placa = placa;
    }

    /**
     * @return the preco
     */
    public String getPreco() {
        return preco;
    }

    /**
     * @param preco the preco to set
     */
    public void setPreco(String preco) {
        this.preco = preco;
    }

}
